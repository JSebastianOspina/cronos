    public function updateRole(Request $request)
    {
        $user = User::find($request->input('user_id'));
        if ($user === null) {
            return response()->json([
                'error' => 'Usuario no encontrado'
            ], 404);
        }
        $user->role = $request->input('role');
        $user->save();

        return response('',201);

    }

